#include "queue.h"

int enqueue(Queue *queue, const void *data) {

  return list_insert_next((List *) queue,list_tail(queue),data) ;

}

int dequeue(Queue *queue, void **data) {

  return list_remove_next((List *) queue,NULL,data) ;

}
